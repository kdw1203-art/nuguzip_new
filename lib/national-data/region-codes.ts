/** 5자리 시군구코드 → SigunguInfo */
const BY_SIGUNGU_CD = new Map<string, SigunguInfo>(
  RAW.map(([intgCd, sigunguCd, sido, sigungu, isNonAutonomous]) => [
    sigunguCd,
    { sigunguCd, intgCd, sido, sigungu, isNonAutonomous },
  ])
);

/** 4자리 통합분류코드 → SigunguInfo[] (여러 시군구가 같은 intgCd를 가질 수 있음) */
const BY_INTG_CD = new Map<string, SigunguInfo[]>();
for (const row of RAW) {
  const info: SigunguInfo = {
    intgCd: row[0], sigunguCd: row[1], sido: row[2], sigungu: row[3], isNonAutonomous: row[4],
  };
  const arr = BY_INTG_CD.get(row[0]) ?? [];
  arr.push(info);
  BY_INTG_CD.set(row[0], arr);
}

/**
 * 지역명(구·시·도)으로 5자리 sigunguCd 검색.
 * MOLIT API의 LAWD_CD 파라미터와 건축HUB의 sigunguCd에 사용.
 * 서울 외 지역 포함 전국 조회 가능.
 */
export function resolveSigunguCd(label?: string): string {
  if (!label?.trim()) return "11680"; // 기본: 강남구
  const q = label.trim().replace(/특별시$|광역시$|특별자치시$|도$/, "").trim();

  // 정확 일치 (sigungu 필드)
  for (const [, info] of BY_SIGUNGU_CD) {
    if (info.sigungu === q || info.sigungu === label.trim()) return info.sigunguCd;
  }
  // 부분 일치 (구·시·군 명)
  for (const [, info] of BY_SIGUNGU_CD) {
    const name = info.sigungu.replace(/^.*?\s/, ""); // 시 이름 제거한 구·군·시
    if (name === q || info.sigungu.includes(q) || q.includes(name)) return info.sigunguCd;
  }
  return "11680";
}

/** sigunguCd → 통합분류코드(intgCd) */
export function resolveIntgCd(sigunguCd: string): string {
  return BY_SIGUNGU_CD.get(sigunguCd)?.intgCd ?? "1024";
}

/** sigunguCd → SigunguInfo */
export function getSigunguInfo(sigunguCd: string): SigunguInfo | undefined {
  return BY_SIGUNGU_CD.get(sigunguCd);
}

/** 모든 시도 목록 (중복 제거) */
export function getAllSido(): string[] {
  const set = new Set<string>();
  for (const [, info] of BY_SIGUNGU_CD) set.add(info.sido);
  return [...set];
}

/** 시도별 시군구 목록 */
export function getSigunguBySido(sido: string): SigunguInfo[] {
  return [...BY_SIGUNGU_CD.values()].filter(i => i.sido === sido);
}
